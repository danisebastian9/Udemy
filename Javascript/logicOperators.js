// or || , and  &&, not !

const resultadoOr =  false || false || false || false || true
console.log(resultadoOr); // true. It's going to always evaluate to true first. 

const resultadoAnd = true && true && false
console.log(resultadoAnd); // false. It's going to always evaluate false first. 

const resultadoNot = !true 
console.log(resultadoNot) // false. It's going to evaluate to the opposite value

// 0 evaluates to false, 1 to true. 


